                                <h3>SEO paslaugos, kokią naudą gausite Jūs:</h3>
                                <ol>
                                    <li><strong>Tiksliniai vartotojai patys suras Jus</strong>. 96% visų  interneto naršytojų ieško informacijos per <a href="http://www.google.lt" title="Google.lt">Google</a>. Ieškodami pagal jūsų veiklos srities raktažodžius - atranda Jūsų tinklapį Google paieškos sistemos top rezultatuose. (arba naudokitės <a href="http://www.seopaslaptys.lt" title="Google reklama"><strong>google reklama</strong></a>)</li>
                                    <li><strong>Žinomumo padidėjimas</strong>.</li>
                                    <li><strong>Tinklapio ir/arba elektroninės parduotuvės lankomumo padidėjimas</strong>.   Atitinkamai užklausų kiekis. Kuo aukštesnėse Google pozicijose bus Jūsų   tinklapis, tuo daugiau tikslinės auditorijos apsilankys, tuo didės ir   Jūsų pardavimai.</li>
                                    <li><strong>Pasitikėjimas paieškos rezultatais</strong>. Naršytojai daugiausia naršo tuos puslapius, kuriuos google išmeta natūralioje paieškoje, bet ne per reklamą.</li>
                                    <li><strong>Pastovumas.</strong> Jūsų tinklapio ir/ar <strong>elektroninės parduotuvės</strong> matomumas ir lankomumas bus stabilesnis ir pastovesnis, kai Jūsų   svetainė bus pirmaujančiose google pozicijose (natūralioje paieškoje).</li>
                                </ol>
                                <h3>SEO Jūsų internetinei parduotuvei</h3>
                                <p>Daugiausiai   internetinėse/elektroninėse parduotuvėse apsiperkančiųjų informacijos   apie prekes ieško per paieškos sistemas. Ir tikriausiai nepaslaptis,kad   dažniausiai pasirenka google. Taigi būvimas kuo geresnėse google   pozicijose duoda žymiai didesnį lankytojų, o kartu ir pirkėjų srautą iš   Jūsų parduotuvės. Galima teigti, jei Jūsų internetinės parduotuvės nėra   pirmame google puslapyje ( t.y. tarp pirmų 10 paieškos rezultatų) , tai   Jūsų nėra internete visai.</p>
                                <h3>Kaip daromas SEO?</h3>
                                <p>SEO (angl. search engine optimization) susideda iš dviejų pagrindinių dalių - <strong><em>vidinė puslapio optimizacija</em></strong> bei <strong><em>išorinė puslapio optimizacija</em></strong>. Atliekant <strong>vidinę puslapio</strong> ar internetinės parduotuvės optimizaciją svetainės struktūra,   navigacija, tekstas optimizuojamas taip, kad būtų visa aplinka   "draugiška" google paieškos sistemai. <strong>Išorinė puslapio optimizacija</strong>,   tai puslapio žinomumo didinimas internete, kartu gerinant ir pozicijas   paieškos sistemose. Taip sudaro nuorodų talpinimą (t.y. nuorodos su   raktiniai žodžiais į Jūsų svetainę ar internetinę parduotuvę su   panaudojant raktinį žodį), socialinių tinklų marketingą (popuiariausias   šiuo metu facebook'as), blogų kūrimą (dažniausiai skirti paieškos   sistemų robotams, bet ne žmonėms) ir pan. </p>
                                <h3>Kiek laiko daroma SEO?</h3>
                                <p>SEO (angl.   search engine optimization) yra tęstinis rezultatas ir tokia sąvoka,   kaip pabaigtas SEO tikrai nėra. Kodėl? Todėl, kad internete pastoviai   kas nors vyksta, susikuria naujos <strong><a href="http://www.el-parduotuve.lt/" title="elektroninės parduotuvės">elektroninės parduotuvės</a></strong> ar tiesiog web svetainės, gerina SEO optimzacija esamos, be to nuolat   yra tobulinamas ir keičiamas google paieškos algoritmas, todėl ir Jūsų   pozicija paieškos sistemų rezultatuose gali nuolat kisti.</p>
                                <p>Taigi   nesitikėkite pasiekti gerų rezultatų per keletą dienų, tai trunka   savaites, o kartai ir keletą mėnesių. Kodėl toks didelis laiko   skirtumas? Viskas priklauso nuo Jūsų konkurentų. Priklauso ar Jūsų   konkurentai gerai atliko vidinę puslapio optimizaciją, kiek laiko   svetainei, kiek lankytojų lanosi ir t.t. Be to nepakanka optimizuoti   internetinę svetainę iš vidaus (bet žinoma reikia nuo to pradėti),   sekantis žingsnis yra išorinė optimizaciją, kuri jums padė pakelti Jūsų   svetainės angl. Page rank.</p>
                                <p>Internetinės parduotuvės, <strong><a href="http://www.el-parduotuve.lt/" title="elektroninės parduotuvės kūrimas">elektroninės parduotuvės kūrimas</a></strong>, o SEO patarimai Jums nemokamai.</p>


