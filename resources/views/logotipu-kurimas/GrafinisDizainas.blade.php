                    <p>Grafinis dizainas &ndash; vizualinio turinio pateikimas įvairiomis meninėmis ir techninėmis priemonėmis[1]. Grafinis dizainas paprastai susijęs ne tik su meninių vaizdų kūrimu, bet ir su analize, organizavimu ir pateikimo metodais vizualinės komunikacijos problemų sprendimams. Tai tarpdisciplininė komercinių ir nekomercinių institucijų vizualinės komunikacijos problemų sprendimo veikla, kurioje reikia suderinti turinio vizualinį kūrybi&scaron;kumą su komunikacijos, technologijų ir verslo žiniomis.&nbsp;<br />
